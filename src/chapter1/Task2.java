package chapter1;

public class Task2 {
//2.Convert Celsius to Fahrenheit
//Formulas: Fahrenheit = Celsius * 9/5 + 32.
public static void main(String[] args) {
    double celsiusTemperature = 77;
    double fahrenheitTemperature =celsiusTemperature *  9.0/5.0 + 32;
    System.out.println("For celsius temperature " + celsiusTemperature
            + "  fahrenheit temperature is: " + fahrenheitTemperature);

}
}
